#include "libft.h"

void	ft_putnbrf(int n)
{
	int		p;
	char	*c;

	p = 1;
	c = "0123456789ABCDEFHIJKLMNOPQRSTUVWXYZ";
	while ((n / p) >= 10 || (n / p) <= -10)
		p *= 10;
	while (p > 0)
	{
		ft_putchar_fd(c[(n / p) % 10], 1);
		p /= 10;
	}
}
